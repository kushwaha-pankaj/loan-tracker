// Reference: BS 2000/01/01 = AD 1943/04/14
const AD_REF = new Date(1943, 3, 14) // April 14, 1943

export const BS_MONTHS_EN = ['Baisakh','Jestha','Ashadh','Shrawan','Bhadra','Ashwin','Kartik','Mangsir','Poush','Magh','Falgun','Chaitra']
export const BS_MONTHS_NP = ['बैशाख','जेठ','आषाढ','श्रावण','भाद्र','आश्विन','कार्तिक','मंसिर','पौष','माघ','फाल्गुन','चैत्र']
export const BS_MIN_YEAR = BS_START_YEAR
export const BS_MAX_YEAR = BS_START_YEAR + BS_DATA.length - 1

function getYearData(bsYear) {
  return BS_DATA[bsYear - BS_START_YEAR] || null
}

/** Days in a given BS month */
export function bsDaysInMonth(year, month) {
  const d = getYearData(year)
  return d ? d[month - 1] : 30
}

/** Total days in a BS year */
export function bsDaysInYear(year) {
  const d = getYearData(year)
  return d ? d.reduce((a, b) => a + b, 0) : 365
}

/**
 * Convert BS date to AD Date object
 */
export function bsToAd(year, month, day) {
  if (!getYearData(year)) return null
  let days = 0
  // Full years from reference to target
  for (let y = BS_START_YEAR; y < year; y++) {
    days += bsDaysInYear(y)
  }
  // Full months in target year
  for (let m = 1; m < month; m++) {
    days += bsDaysInMonth(year, m)
  }
  days += day - 1

  const result = new Date(AD_REF)
  result.setDate(result.getDate() + days)
  return result
}

/**
 * Convert AD Date to BS { year, month, day }
 */
export function adToBs(date) {
  const d = new Date(date)
  d.setHours(0, 0, 0, 0)
  const ref = new Date(AD_REF)
  ref.setHours(0, 0, 0, 0)

  let days = Math.round((d - ref) / 86400000)
  if (days < 0) return null

  let year = BS_START_YEAR
  while (true) {
    const yd = bsDaysInYear(year)
    if (days < yd) break
    days -= yd
    year++
    if (year > BS_MAX_YEAR) return null
  }

  let month = 1
  while (true) {
    const md = bsDaysInMonth(year, month)
    if (days < md) break
    days -= md
    month++
  }

  return { year, month, day: days + 1 }
}

/** Today's date in BS */
export function todayBs() {
  return adToBs(new Date())
}

/** Format BS date as string */
export function formatBs(y, m, d) {
  return `${y}/${String(m).padStart(2,'0')}/${String(d).padStart(2,'0')}`
}

/** Days between two BS dates (end - start) */
export function daysBetweenBs(y1,m1,d1, y2,m2,d2) {
  const a = bsToAd(y1,m1,d1)
  const b = bsToAd(y2,m2,d2)
  if (!a || !b) return 0
  return Math.round((b - a) / 86400000)
}
